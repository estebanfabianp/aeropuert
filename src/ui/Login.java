    private void txtnombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnombreKeyTyped
        char soloNumero = evt.getKeyChar();
        if (soloNumero < '0' || soloNumero > '9') {
            evt.consume(); //validacion de solo numero
            JOptionPane.showMessageDialog(null, "solo se permite numeros", "Advertemvia", JOptionPane.ERROR_MESSAGE);
            txtnombre.setText("");
            txtnombre.requestFocus();
        }
        if (txtnombre.getText().length() == 12) {
            ms1.setVisible(true);
            mg1.setVisible(true);
            txtnombre.setText("");
            txtnombre.requestFocus();
        }
        else if (txtnombre.getText().length() == 11)
        {
             
            ms1.setVisible(false);
            mg1.setVisible(false);
            
        }        
    }//GEN-LAST:event_txtnombreKeyTyped

